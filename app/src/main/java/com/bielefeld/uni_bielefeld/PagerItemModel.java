package com.bielefeld.uni_bielefeld;


public class PagerItemModel {
    private int image;
    private int title;

    public PagerItemModel(int image, int title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public int getTitle() {
        return title;
    }
}



